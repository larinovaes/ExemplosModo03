package ExemploAnimal;

public class Gato {
    String nome = "Vitim";
    String pelagem;
    String raca;
    double tamanho;
    double peso;

    public Gato (String nome, double tamanho, String raca, String pelagem, double peso){
        this.pelagem = pelagem;
        this.raca = raca;
        this.tamanho = tamanho;
        this.peso = peso;
        this.nome = nome;
    }
}
